    // Manejar el caso en que no se encuentre el usuario en la base de datos
    $nombre = "Nombre";
    $apellido = "Apellido";
    $correo = "Correo_electronico";
    $contrasena = "Contraseña";
}

// Cerrar la conexión a la base de datos
$conn->close();
